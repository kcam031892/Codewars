function stringToArray(string) {
  // split the string and return.
  return string.split(' ');
}

console.log(stringToArray('Robin Singh'), ['Robin', 'Singh']);
console.log(stringToArray('I love arrays they are my favorite'), [
  'I',
  'love',
  'arrays',
  'they',
  'are',
  'my',
  'favorite',
]);
